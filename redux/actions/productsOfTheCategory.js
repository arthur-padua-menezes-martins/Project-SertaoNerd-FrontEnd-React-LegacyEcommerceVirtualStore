/* basic modules************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
import axios from 'axios'
import config from '../../config.js'

/* types************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
import { GET_PRODUCTS_OF_THE_CATEGORIES_DEFAULT } from '../types.js'
axios.defaults.withCredentials = true

/* return************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
export default {

  getProductsOfTheCategory_default: (category, offset, limit) => dispatch => {
    return axios.get(`${config.api}/search/${category}?paginate=true&offset=${offset}&limit=${limit}`).then(response =>

      dispatch({ type: GET_PRODUCTS_OF_THE_CATEGORIES_DEFAULT, payload: response.data })

    ).catch(error => console.error(error))
  }

}
