/* basic modules************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
import axios from 'axios'
import config from '../../config.js'

/* types************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
import { GET_HOME_PAGE_PRODUCTS } from '../types.js'
axios.defaults.withCredentials = true

/* return************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
export default {

  getHomePageProducts: (category, type, collections) => dispatch => (
    axios.get(`${config.api}/search/${category}/${type}?collections=${collections}&offset=0&limit=6`).then(response =>

      dispatch({ type: GET_HOME_PAGE_PRODUCTS, payload: response.data })

    ).catch(error => console.error(error))
  )

}
