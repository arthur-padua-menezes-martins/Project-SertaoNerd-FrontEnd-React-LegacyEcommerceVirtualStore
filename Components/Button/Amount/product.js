/*basic modules************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
import React from 'react'

/*styled components************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
import { StyledContainerButtonAmountProduct, StyledArticleButtonAmountProduct } from './styles.js'

/*main function************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
function ButtonAmountProduct(props) {

    var { amount, color, justifyContent, containerWidth, border = true, onClick, children } = props

    return (
        <StyledContainerButtonAmountProduct justifyContent={justifyContent} width={containerWidth}>

            <section>

                <StyledArticleButtonAmountProduct border={border}>

                    <input
                        style={{ color: color }}
                        value={amount || 1}
                        type='text'
                    />

                    <div>
                        <div>
                            <button
                                style={{ color: color }}
                                onClick={() => onClick(++amount)}>▲</button>
                            <button
                                style={{ color: color }}
                                onClick={() => onClick(amount !== 1 ? --amount : amount)}>▼</button>
                        </div>
                    </div>

                </StyledArticleButtonAmountProduct>

            </section>

            {children}

        </StyledContainerButtonAmountProduct>
    )
}
export default ButtonAmountProduct