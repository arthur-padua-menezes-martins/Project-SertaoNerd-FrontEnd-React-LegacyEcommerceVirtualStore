/*basic modules************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
import React from 'react'
import Link from 'next/link'

/*redux************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
import { connect } from 'react-redux'
import actions from '../../redux/actions/index.js'

/*styled components************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
import { StyledSectionHeaderContainerMobile } from './styles.js'

/*main function************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
const MobileHeaderProfile = (props) => { console.log(props.login)

    if (typeof props.login.authentication.authenticated !== 'undefined') {
        return (
            <StyledSectionHeaderContainerMobile>

                { props.login.authentication.authenticated && (

                    <Link href='/account/perfil'>
                        <div>
                            <span><i className='fa fa-user' /></span>
                            <span>Perfil</span>
                        </div>
                    </Link>
                )}

                { !props.login.authentication.authenticated && (
                    <Link href='/login'>

                        <div>
                            <span><img src="/svg/mobile_lock.svg" alt="lock"/></span>
                            <span>Logar-se</span>
                        </div>
                    </Link>
                )}

            </StyledSectionHeaderContainerMobile>
        )
    } else {
        return (
            <></>
        )
    }
}

const mapStateToProps = (state, ownProps) => ({
    ...state
})
export default connect(mapStateToProps, actions)(MobileHeaderProfile)