/*basic modules************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
import React from 'react'

/*redux************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
import { connect } from 'react-redux'
import actions from '../../redux/actions/index.js'

/*styled components************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
import { StyledSectionHeaderContainerMobile } from './styles.js'

/*main function************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
const MobileHeaderCart = props => {

    return (
        <>

            {typeof props.login.authentication.authenticated !== 'undefined' ?
                (<>

                    <StyledSectionHeaderContainerMobile>
                        <div onClick={() => window.location.href = props.login.authentication.authenticated ? '/cart' : '/login?authenticated=false'}>
                            <span><img src="/svg/mobile_cart.svg"/></span>
                            <span>Carrinho</span>
                        </div>
                    </StyledSectionHeaderContainerMobile>

                </>) :
                (<>

                    <StyledSectionHeaderContainerMobile>
                        <div onClick={() => window.location.href = '/login?authenticated=false'}>
                            <span><img src="/svg/mobile_cart.svg"/></span>
                            <span>Carrinho</span>
                        </div>
                    </StyledSectionHeaderContainerMobile>

                </>)
            }

        </>
    )
}

const mapStateToProps = (state, ownProps) => ({
    ...state
})
export default connect(mapStateToProps, actions)(MobileHeaderCart)