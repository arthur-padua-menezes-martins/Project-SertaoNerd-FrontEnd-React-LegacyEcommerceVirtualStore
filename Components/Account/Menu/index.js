/*basic modules************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
import React from 'react'
import Link from 'next/link'

/*styled components************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
import {
    StyledContainerMenuAccount, StyledSectionMenuAccount,
    StyledSectionMenuAccountRow, StyledDivMenuAccountData
} from './styles.js'

/*helpers************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
import accountMenu from '../../../helpers/account/menu/index.js'

/*main function************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
function MenuAccount({ informations, selected, containerWidth = '100%' }) {

    const accountMenuInformations = accountMenu.accountMenuInformations

    return (
        <StyledContainerMenuAccount containerWidth={containerWidth}>

            <StyledSectionMenuAccount selected={selected} maxHeight={accountMenuInformations.length}>

                {accountMenuInformations.map((information, index) => (

                    <span onClick={() => window.location.href = information.url}>

                        <StyledSectionMenuAccountRow key={index}>

                            <StyledDivMenuAccountData>{information.icon}</StyledDivMenuAccountData>
                            <StyledDivMenuAccountData>{information.name}</StyledDivMenuAccountData>

                        </StyledSectionMenuAccountRow>

                    </span>
                ))}

            </StyledSectionMenuAccount>

        </StyledContainerMenuAccount>
    )

}
export default MenuAccount