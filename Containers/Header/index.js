/*basic modules************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
import React, { useState, useEffect, useLayoutEffect } from 'react'

/*containers************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
import DesktopHeader from './desktop.js'
import MobileHeader from './mobile.js'

/*main function***********************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
function Header(props) {

    /*functions************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
    window.addEventListener('resize', () => {
        props.setInitialInnerWidth(globalThis.innerWidth)
    })

    /*render functions************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
    function renderHeaderDefaultDesktop() {
        return (
            <DesktopHeader Default={true} />
        )
    }
    function renderHeaderSimpleDesktop() {
        return (
            <DesktopHeader Simple={true} />
        )
    }
    function renderHeaderDefaultMobile() {
        return (
            <MobileHeader Default={true} />
        )
    }
    function renderHeaderSimpleMobile() {
        return (
            <MobileHeader Simple={true} />
        )
    }
    function renderHeaderAccountMobile() {
        return (
            <MobileHeader Account={true} />
        )
    }

    /*return************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
    if (props.Desktop && props.Default) { return (renderHeaderDefaultDesktop()) }
    if (props.Desktop && props.Simple) { return (renderHeaderSimpleDesktop()) }
    if (props.Mobile && props.Default) { return (renderHeaderDefaultMobile()) }
    if (props.Mobile && props.Simple) { return (renderHeaderSimpleMobile()) }
    if (props.Mobile && props.Account) { return (renderHeaderAccountMobile()) }

}
export default Header