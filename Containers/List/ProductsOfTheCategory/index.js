/*basic modules************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
import React, { useState } from 'react'

/*components************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
import CategoryProductsDesktop from './desktop.js'
import Pagination from '../../Pagination/index.js'

/*helpers************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
import ProductsObject from '../../../helpers/objects/products.js'

/*main function************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
function CategoryProducts({ informations, offset, limit, productsByLine, thisPage, setThisPage }) {


    /*hooks************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
    var
        [Products, setProducts] = useState(
            [...ProductsObject.releases(), ...ProductsObject.releases()]
        )




    /*return************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************/
    return (
        <>
            <CategoryProductsDesktop
                informations={informations}
                Products={Products}
                offset={offset}
                limit={limit}
                productsByLine={productsByLine}
            />
            <Pagination
                thisPage={thisPage}
                setThisPage={setThisPage}
                pages={Math.ceil( informations.total / limit )}
            />
        </>
    )
}
export default CategoryProducts